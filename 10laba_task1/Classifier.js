function changeColorText(text)
{
    document.getElementById(text).style.color = "white";
    document.getElementById(text).style.backgroundColor = "#345";
    document.getElementById(text).style.font = "calibri";
}
function changeColorHead(head)
{
    document.getElementById(head).style.color = "#345";
    document.getElementById(head).style.fontFamily = "Times New Roman";
}